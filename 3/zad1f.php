<?php
    function add($firstNumber, $secondNumber)
    {
        return $firstNumber + $secondNumber;
    }
    function sub($firstNumber, $secondNumber)
    {
        return $firstNumber - $secondNumber;
    }
    function mul($firstNumber, $secondNumber)
    {
        return $firstNumber * $secondNumber;
    }
    function div($firstNumber, $secondNumber)
    {
        return $firstNumber / $secondNumber;
    }
?>